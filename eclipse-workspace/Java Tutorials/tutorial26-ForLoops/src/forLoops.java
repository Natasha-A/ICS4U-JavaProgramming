
public class forLoops {

	public static void main(String[] args) {
		
		// ICU - need al sections
		
		//INCREASING
		for(int i = 0; i < 9; i++) { //goes up to i = 9 
			System.out.println(i);
			
		}
		
		System.out.println(" ");
		
		//DECREASING
		for(int i = 9; i >= 0; i--) { //goes down to i = 0
			System.out.println(i);
			
		}

	}

}
